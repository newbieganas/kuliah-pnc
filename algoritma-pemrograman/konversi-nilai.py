print('Konversi Nilai')
nilai = float(input('Masukkan Nilai : '))

if nilai >= 80:
    print('Nilai A')
elif nilai >= 75:
    print('Nilai AB')
elif nilai >= 65:
    print('Nilai B')
elif nilai >= 60:
    print('Nilai BC')
elif nilai >= 50:
    print('Nilai C')
elif nilai >= 40:
    print('Nilai D')
else:
    print('Nilai E')
